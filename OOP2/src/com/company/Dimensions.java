package com.company;

public class Dimensions {
    private int width;
    private int hiefht;
    private  int depth;

    public Dimensions(int width, int hiefht, int depth) {
        this.width = width;
        this.hiefht = hiefht;
        this.depth = depth;
    }

    public int getWidth() {
        return width;
    }

    public int getHiefht() {
        return hiefht;
    }

    public int getDepth() {
        return depth;
    }
}

